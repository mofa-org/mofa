/// Create a `CandleProvider` from a local model directory.
///
/// # Example
///
/// ```rust,ignore
/// let provider = candle_from_path("/models/TinyLlama-1.1B-Chat").await?;
/// ```
pub async fn candle_from_path(path: impl Into<PathBuf>) -> LLMResult<CandleProvider> {
    CandleProvider::load(CandleConfig::new(path)).await
}

/// Create a `CandleProvider` from a path with GPU enabled.
///
/// Falls back to CPU if CUDA is not available.
pub async fn candle_from_path_gpu(path: impl Into<PathBuf>) -> LLMResult<CandleProvider> {
    CandleProvider::load(CandleConfig::new(path).with_gpu(true)).await
}

