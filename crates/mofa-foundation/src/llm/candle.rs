/// Result of a generation run.
struct GenerationResult {
    /// Generated token ids (not including prompt).
    tokens: Vec<u32>,
    /// Decoded text.
    text: String,
    /// Number of prompt tokens.
    prompt_token_count: u32,
    /// Number of generated tokens.
    completion_token_count: u32,
    /// Why generation stopped.
    finish_reason: FinishReason,
    /// Parsed tool calls, if any.
    tool_calls: Option<Vec<ToolCall>>,
}

/// Run the autoregressive generation loop.
fn generate_sync(
    engine: &mut InferenceEngine,
    prompt_tokens: &[u32],
    max_new_tokens: usize,
    temperature: f64,
    top_p: Option<f64>,
    repeat_penalty: f32,
    repeat_last_n: usize,
    seed: u64,
    has_tools: bool,
) -> Result<GenerationResult, LLMError> {
    let map_err = |e: candle_core::Error| LLMError::Other(format!("Candle error: {}", e));

    engine.model.reset();

    let eos_token = engine.tokenizer.eos_token_id();
    let im_end_token = engine
        .tokenizer
        .encode("<|im_end|>")
        .ok()
        .and_then(|ids| ids.last().copied());

    let mut logits_processor = LogitsProcessor::new(seed, Some(temperature), top_p);

    let prompt_len = prompt_tokens.len() as u32;
    let mut all_tokens: Vec<u32> = prompt_tokens.to_vec();
    let mut generated_tokens: Vec<u32> = Vec::with_capacity(max_new_tokens);

    // Prefill: process the entire prompt
    let input = Tensor::new(prompt_tokens, &engine.device).map_err(map_err)?;
    let input = input.unsqueeze(0).map_err(map_err)?;
    let logits = engine.model.forward(&input, 0).map_err(map_err)?;

    // Sample first token from last position
    let logits = logits
        .squeeze(0)
        .map_err(map_err)?
        .to_dtype(DType::F32)
        .map_err(map_err)?;
    // If logits is [seq_len, vocab], take the last position
    let logits = if logits.dims().len() == 2 {
        let seq_len = logits.dim(0).map_err(map_err)?;
        logits
            .narrow(0, seq_len - 1, 1)
            .map_err(map_err)?
            .squeeze(0)
            .map_err(map_err)?
    } else {
        logits
    };
    let logits =
        apply_repeat_penalty(&logits, &all_tokens, repeat_penalty, repeat_last_n)?;
    let mut next_token = logits_processor.sample(&logits).map_err(map_err)?;

    generated_tokens.push(next_token);
    all_tokens.push(next_token);

    if is_stop_token(next_token, eos_token, im_end_token) {
        let text = decode_and_clean(&engine.tokenizer, &generated_tokens)?;
        let tool_calls = if has_tools { parse_tool_calls(&text) } else { None };
        let finish_reason = if tool_calls.is_some() {
            FinishReason::ToolCalls
        } else {
            FinishReason::Stop
        };
        return Ok(GenerationResult {
            tokens: generated_tokens,
            text,
            prompt_token_count: prompt_len,
            completion_token_count: 1,
            finish_reason,
            tool_calls,
        });
    }

    // Autoregressive decode loop
    for i in 1..max_new_tokens {
        let input = Tensor::new(&[next_token], &engine.device).map_err(map_err)?;
        let input = input.unsqueeze(0).map_err(map_err)?;
        let logits = engine
            .model
            .forward(&input, prompt_tokens.len() + i)
            .map_err(map_err)?;

        let logits = logits
            .squeeze(0)
            .map_err(map_err)?
            .to_dtype(DType::F32)
            .map_err(map_err)?;
        // Handle [1, vocab] shape from single-token input
        let logits = if logits.dims().len() == 2 {
            logits.squeeze(0).map_err(map_err)?
        } else {
            logits
        };
        let logits =
            apply_repeat_penalty(&logits, &all_tokens, repeat_penalty, repeat_last_n)?;
        next_token = logits_processor.sample(&logits).map_err(map_err)?;

        generated_tokens.push(next_token);
        all_tokens.push(next_token);

        if is_stop_token(next_token, eos_token, im_end_token) {
            break;
        }
    }

    let completion_count = generated_tokens.len() as u32;
    let finish_reason = if (completion_count as usize) >= max_new_tokens {
        FinishReason::Length
    } else {
        FinishReason::Stop
    };

    let text = decode_and_clean(&engine.tokenizer, &generated_tokens)?;

    let tool_calls = if has_tools { parse_tool_calls(&text) } else { None };
    let finish_reason = if tool_calls.is_some() {
        FinishReason::ToolCalls
    } else {
        finish_reason
    };

    Ok(GenerationResult {
        tokens: generated_tokens,
        text,
        prompt_token_count: prompt_len,
        completion_token_count: completion_count,
        finish_reason,
        tool_calls,
    })
}

/// Streaming generation: sends each token through a channel as it's produced.
fn generate_streaming(
    engine: &mut InferenceEngine,
    prompt_tokens: &[u32],
    max_new_tokens: usize,
    temperature: f64,
    top_p: Option<f64>,
    repeat_penalty: f32,
    repeat_last_n: usize,
    seed: u64,
    model_id: String,
    tx: tokio::sync::mpsc::Sender<LLMResult<ChatCompletionChunk>>,
) {
    let result = generate_streaming_inner(
        engine,
        prompt_tokens,
        max_new_tokens,
        temperature,
        top_p,
        repeat_penalty,
        repeat_last_n,
        seed,
        &model_id,
        &tx,
    );

    if let Err(e) = result {
        let _ = tx.blocking_send(Err(e));
    }
}

fn generate_streaming_inner(
    engine: &mut InferenceEngine,
    prompt_tokens: &[u32],
    max_new_tokens: usize,
    temperature: f64,
    top_p: Option<f64>,
    repeat_penalty: f32,
    repeat_last_n: usize,
    seed: u64,
    model_id: &str,
    tx: &tokio::sync::mpsc::Sender<LLMResult<ChatCompletionChunk>>,
) -> Result<(), LLMError> {
    let map_err = |e: candle_core::Error| LLMError::Other(format!("Candle error: {}", e));

    engine.model.reset();

    let eos_token = engine.tokenizer.eos_token_id();
    let im_end_token = engine
        .tokenizer
        .encode("<|im_end|>")
        .ok()
        .and_then(|ids| ids.last().copied());

    let mut logits_processor = LogitsProcessor::new(seed, Some(temperature), top_p);

    let mut all_tokens: Vec<u32> = prompt_tokens.to_vec();

    let now_secs = || -> u64 {
        std::time::SystemTime::now()
            .duration_since(std::time::UNIX_EPOCH)
            .unwrap_or_default()
            .as_secs()
    };

    let chunk_id = format!("candle-stream-{}", uuid::Uuid::now_v7());

    // Send initial chunk with role
    let initial_chunk = ChatCompletionChunk {
        id: chunk_id.clone(),
        object: "chat.completion.chunk".to_string(),
        created: now_secs(),
        model: model_id.to_string(),
        choices: vec![ChunkChoice {
            index: 0,
            delta: ChunkDelta {
                role: Some(Role::Assistant),
                content: None,
                tool_calls: None,
            },
            finish_reason: None,
        }],
        usage: None,
    };
    if tx.blocking_send(Ok(initial_chunk)).is_err() {
        return Ok(()); // Receiver dropped
    }

    // Prefill
    let input = Tensor::new(prompt_tokens, &engine.device).map_err(map_err)?;
    let input = input.unsqueeze(0).map_err(map_err)?;
    let logits = engine.model.forward(&input, 0).map_err(map_err)?;

    let logits = logits
        .squeeze(0)
        .map_err(map_err)?
        .to_dtype(DType::F32)
        .map_err(map_err)?;
    let logits = if logits.dims().len() == 2 {
        let seq_len = logits.dim(0).map_err(map_err)?;
        logits
            .narrow(0, seq_len - 1, 1)
            .map_err(map_err)?
            .squeeze(0)
            .map_err(map_err)?
    } else {
        logits
    };
    let logits =
        apply_repeat_penalty(&logits, &all_tokens, repeat_penalty, repeat_last_n)?;
    let mut next_token = logits_processor.sample(&logits).map_err(map_err)?;

    all_tokens.push(next_token);

    if is_stop_token(next_token, eos_token, im_end_token) {
        send_final_chunk(tx, &chunk_id, model_id, now_secs(), FinishReason::Stop);
        return Ok(());
    }

    // Send first token
    let token_text = engine.tokenizer.decode_single(next_token)?;
    let token_text = clean_streaming_token(&token_text);
    if !token_text.is_empty() {
        send_content_chunk(tx, &chunk_id, model_id, now_secs(), &token_text);
    }

    // Autoregressive decode loop
    for i in 1..max_new_tokens {
        let input = Tensor::new(&[next_token], &engine.device).map_err(map_err)?;
        let input = input.unsqueeze(0).map_err(map_err)?;
        let logits = engine
            .model
            .forward(&input, prompt_tokens.len() + i)
            .map_err(map_err)?;

        let logits = logits
            .squeeze(0)
            .map_err(map_err)?
            .to_dtype(DType::F32)
            .map_err(map_err)?;
        let logits = if logits.dims().len() == 2 {
            logits.squeeze(0).map_err(map_err)?
        } else {
            logits
        };
        let logits =
            apply_repeat_penalty(&logits, &all_tokens, repeat_penalty, repeat_last_n)?;
        next_token = logits_processor.sample(&logits).map_err(map_err)?;
        all_tokens.push(next_token);

        if is_stop_token(next_token, eos_token, im_end_token) {
            send_final_chunk(tx, &chunk_id, model_id, now_secs(), FinishReason::Stop);
            return Ok(());
        }

        let token_text = engine.tokenizer.decode_single(next_token)?;
        let token_text = clean_streaming_token(&token_text);
        if !token_text.is_empty() {
            if tx
                .blocking_send(Ok(ChatCompletionChunk {
                    id: chunk_id.clone(),
                    object: "chat.completion.chunk".to_string(),
                    created: now_secs(),
                    model: model_id.to_string(),
                    choices: vec![ChunkChoice {
                        index: 0,
                        delta: ChunkDelta {
                            role: None,
                            content: Some(token_text),
                            tool_calls: None,
                        },
                        finish_reason: None,
                    }],
                    usage: None,
                }))
                .is_err()
            {
                return Ok(()); // Receiver dropped
            }
        }
    }

    // Max tokens reached
    send_final_chunk(tx, &chunk_id, model_id, now_secs(), FinishReason::Length);
    Ok(())
}

