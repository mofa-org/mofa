/// Clean streaming tokens of ChatML artifacts.
fn clean_streaming_token(token: &str) -> String {
    if token.contains("<|im_end|>")
        || token.contains("<|endoftext|>")
        || token.contains("</s>")
        || token.contains("<|im_start|>")
    {
        String::new()
    } else {
        token.to_string()
    }
}

fn is_stop_token(token: u32, eos: Option<u32>, im_end: Option<u32>) -> bool {
    if let Some(eos_id) = eos {
        if token == eos_id {
            return true;
        }
    }
    if let Some(im_end_id) = im_end {
        if token == im_end_id {
            return true;
        }
    }
    false
}

fn apply_repeat_penalty(
    logits: &Tensor,
    all_tokens: &[u32],
    penalty: f32,
    last_n: usize,
) -> Result<Tensor, LLMError> {
    if penalty <= 1.0 || last_n == 0 || all_tokens.is_empty() {
        return Ok(logits.clone());
    }

    let map_err = |e: candle_core::Error| LLMError::Other(format!("Penalty error: {}", e));

    let start = all_tokens.len().saturating_sub(last_n);
    let penalty_tokens = &all_tokens[start..];

    candle_transformers::utils::apply_repeat_penalty(logits, penalty, penalty_tokens)
        .map_err(map_err)
}

/// Decode tokens and clean up ChatML artifacts.
fn decode_and_clean(tokenizer: &TokenizerWrapper, tokens: &[u32]) -> Result<String, LLMError> {
    let text = tokenizer.decode(tokens)?;
    Ok(text
        .trim()
        .trim_end_matches("<|im_end|>")
        .trim_end_matches("<|endoftext|>")
        .trim_end_matches("</s>")
        .trim_end_matches("<|eot_id|>")
        .trim()
        .to_string())
}

