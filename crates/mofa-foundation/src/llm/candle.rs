/// Thin wrapper around a tokenizer loaded from `tokenizer.json`.
struct TokenizerWrapper {
    inner: tokenizers::Tokenizer,
}

impl TokenizerWrapper {
    fn from_file(path: &Path) -> Result<Self, LLMError> {
        let inner = tokenizers::Tokenizer::from_file(path).map_err(|e| {
            LLMError::ConfigError(format!("Failed to load tokenizer from {:?}: {}", path, e))
        })?;
        Ok(Self { inner })
    }

    fn encode(&self, text: &str) -> Result<Vec<u32>, LLMError> {
        let encoding = self
            .inner
            .encode(text, true)
            .map_err(|e| LLMError::Other(format!("Tokenization failed: {}", e)))?;
        Ok(encoding.get_ids().to_vec())
    }

    fn decode(&self, tokens: &[u32]) -> Result<String, LLMError> {
        self.inner
            .decode(tokens, true)
            .map_err(|e| LLMError::Other(format!("Detokenization failed: {}", e)))
    }

    /// Decode a single token to text (used for streaming).
    fn decode_single(&self, token: u32) -> Result<String, LLMError> {
        self.decode(&[token])
    }

    /// Return the EOS token id, if the tokenizer defines one.
    fn eos_token_id(&self) -> Option<u32> {
        let vocab = self.inner.get_added_vocabulary().get_vocab();
        vocab
            .get("</s>")
            .copied()
            .or_else(|| vocab.get("<|endoftext|>").copied())
            .or_else(|| vocab.get("<|end|>").copied())
            .or_else(|| vocab.get("<|eot_id|>").copied())
    }

    /// Return the vocab size.
    fn vocab_size(&self) -> usize {
        self.inner.get_vocab_size(true)
    }
}

