/// Everything needed to run inference. Wrapped in `Arc<Mutex<_>>` inside
/// the provider because Candle model state is not `Sync`.
struct InferenceEngine {
    model: Box<dyn CandleModel>,
    tokenizer: TokenizerWrapper,
    device: Device,
    config: CandleConfig,
    hidden_size: usize,
}

/// Trait that abstracts over different Candle model forward passes.
trait CandleModel: Send {
    /// Run a forward pass and return logits of shape `[1, seq_len, vocab_size]`
    /// or `[1, vocab_size]`.
    fn forward(
        &mut self,
        input_ids: &Tensor,
        position: usize,
    ) -> Result<Tensor, candle_core::Error>;

    /// Run a forward pass and return the last hidden states of shape
    /// `[1, seq_len, hidden_size]` for embedding extraction.
    /// Default implementation returns an error.
    fn forward_hidden(
        &mut self,
        input_ids: &Tensor,
        position: usize,
    ) -> Result<Tensor, candle_core::Error> {
        // Fall back to regular forward — some architectures expose this,
        // others don't. We use the logits forward as a rough approximation
        // when hidden states aren't available.
        let _ = (input_ids, position);
        Err(candle_core::Error::Msg(
            "forward_hidden not implemented for this architecture".to_string(),
        ))
    }

    /// Reset any KV-cache between requests.
    fn reset(&mut self);
}

