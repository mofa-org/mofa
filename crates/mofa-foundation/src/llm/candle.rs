/// Configuration for the Candle local inference provider.
#[derive(Debug, Clone)]
pub struct CandleConfig {
    /// Path to the model directory (must contain config.json, tokenizer.json,
    /// and model weight files in SafeTensors format).
    pub model_path: PathBuf,

    /// Logical model identifier surfaced via `default_model()` and embedded in
    /// responses. Defaults to the directory name of `model_path`.
    pub model_id: String,

    /// Default temperature for sampling. 0.0 = greedy.
    pub default_temperature: f64,

    /// Default maximum number of tokens to generate.
    pub default_max_tokens: usize,

    /// Top-p (nucleus) sampling threshold. `None` disables it.
    pub top_p: Option<f64>,

    /// Repetition penalty applied to already-generated tokens.
    pub repeat_penalty: f32,

    /// Number of recent tokens considered for the repetition penalty window.
    pub repeat_last_n: usize,

    /// Random seed for reproducibility. `None` uses a random seed.
    pub seed: Option<u64>,

    /// Whether to attempt GPU (CUDA) acceleration.
    pub use_gpu: bool,

    /// Data type for model weights. `None` = auto-detect from model files.
    pub dtype: Option<DType>,

    /// Tool calling format to use when tools are present.
    pub tool_format: ToolCallFormat,

    /// Embedding pooling strategy.
    pub embedding_pooling: EmbeddingPooling,
}

/// Format for tool calling in the prompt.
#[derive(Debug, Clone, Default)]
pub enum ToolCallFormat {
    /// ChatML-style tool format (Hermes-2-Pro, Llama-3.1)
    #[default]
    ChatML,
    /// Raw JSON function call format
    RawJson,
}

/// Pooling strategy for embedding extraction.
#[derive(Debug, Clone, Default)]
pub enum EmbeddingPooling {
    /// Mean of all token hidden states
    #[default]
    Mean,
    /// First token ([CLS] / BOS) hidden state
    FirstToken,
    /// Last token hidden state
    LastToken,
}

impl CandleConfig {
    /// Create a minimal configuration pointing at a local model directory.
    pub fn new(model_path: impl Into<PathBuf>) -> Self {
        let path: PathBuf = model_path.into();
        let model_id = path
            .file_name()
            .map(|n| n.to_string_lossy().to_string())
            .unwrap_or_else(|| "candle-model".to_string());

        Self {
            model_path: path,
            model_id,
            default_temperature: 0.7,
            default_max_tokens: 512,
            top_p: Some(0.9),
            repeat_penalty: 1.1,
            repeat_last_n: 64,
            seed: Some(42),
            use_gpu: false,
            dtype: None,
            tool_format: ToolCallFormat::default(),
            embedding_pooling: EmbeddingPooling::default(),
        }
    }

    pub fn with_model_id(mut self, id: impl Into<String>) -> Self {
        self.model_id = id.into();
        self
    }

    pub fn with_temperature(mut self, temp: f64) -> Self {
        self.default_temperature = temp;
        self
    }

    pub fn with_max_tokens(mut self, tokens: usize) -> Self {
        self.default_max_tokens = tokens;
        self
    }

    pub fn with_top_p(mut self, top_p: f64) -> Self {
        self.top_p = Some(top_p);
        self
    }

    pub fn with_repeat_penalty(mut self, penalty: f32) -> Self {
        self.repeat_penalty = penalty;
        self
    }

    pub fn with_repeat_last_n(mut self, n: usize) -> Self {
        self.repeat_last_n = n;
        self
    }

    pub fn with_seed(mut self, seed: u64) -> Self {
        self.seed = Some(seed);
        self
    }

    pub fn with_gpu(mut self, enabled: bool) -> Self {
        self.use_gpu = enabled;
        self
    }

    pub fn with_dtype(mut self, dtype: DType) -> Self {
        self.dtype = Some(dtype);
        self
    }

    pub fn with_tool_format(mut self, format: ToolCallFormat) -> Self {
        self.tool_format = format;
        self
    }

    pub fn with_embedding_pooling(mut self, pooling: EmbeddingPooling) -> Self {
        self.embedding_pooling = pooling;
        self
    }
}

