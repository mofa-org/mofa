// Llama implementation
// ---------------------------------------------------------------------------

struct LlamaModel {
    model: candle_transformers::models::llama::Llama,
    cache: candle_transformers::models::llama::Cache,
}

impl CandleModel for LlamaModel {
    fn forward(
        &mut self,
        input_ids: &Tensor,
        position: usize,
    ) -> Result<Tensor, candle_core::Error> {
        self.model.forward(input_ids, position, &mut self.cache)
    }

    fn reset(&mut self) {
        self.cache.clear();
    }
}

