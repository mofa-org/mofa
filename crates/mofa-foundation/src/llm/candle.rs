//! Candle Provider — fully local, native Rust LLM inference
//!
//! Uses Hugging Face Candle for on-device transformer inference with no
//! HTTP calls, no Python, no external servers, and no API keys.
//!
//! # Capabilities
//!
//! | Feature     | Status |
//! |-------------|--------|
//! | Chat        | ✅     |
//! | Streaming   | ✅     |
//! | Tool calls  | ✅     |
//! | Embeddings  | ✅     |
//! | Vision      | ❌     |
//!
//! # Supported model formats
//!
//! - SafeTensors (`.safetensors`)
//! - Auto-detected architectures: Llama, Mistral, Phi, Qwen2
//!
//! # Quick start
//!
//! ```rust,ignore
//! use mofa_foundation::llm::candle::{CandleConfig, CandleProvider};
//!
//! let config = CandleConfig::new("/path/to/model")
//!     .with_model_id("my-local-model")
//!     .with_max_tokens(512);
//!
//! let provider = CandleProvider::load(config).await?;
//! let response = provider.chat(request).await?;
//! ```
//!
//! # Streaming
//!
//! ```rust,ignore
//! use futures::StreamExt;
//!
//! let mut stream = provider.chat_stream(request).await?;
//! while let Some(chunk) = stream.next().await {
//!     match chunk {
//!         Ok(c) => print!("{}", c.choices[0].delta.content.as_deref().unwrap_or("")),
//!         Err(e) => eprintln!("Error: {}", e),
//!     }
//! }
//! ```
//!
//! # Tool calling
//!
//! The provider detects tool call JSON in model output when tools are provided
//! in the request. Models must be instruction-tuned for function calling
//! (e.g. Llama-3.1-Instruct, Mistral-Instruct, Hermes-2-Pro).
//!
//! # Embeddings
//!
//! Embeddings are generated by running a forward pass and mean-pooling the
//! last hidden state. Works best with embedding-specific models, but any
//! transformer model can produce embeddings.
//!
//! # Feature gate
//!
//! This module is only available when the `linux-candle` feature is enabled:
//!
//! ```toml
//! mofa-foundation = { version = "0.1", features = ["linux-candle"] }
//! ```

