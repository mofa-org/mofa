/// Convert MoFA `ChatMessage` list into a single prompt string.
///
/// Uses ChatML format that works with most instruction-tuned models
/// (Llama-3, Mistral-Instruct, Phi-3, Qwen-2, Hermes-2-Pro, etc.):
///
/// ```text
/// <|im_start|>system
/// You are helpful.<|im_end|>
/// <|im_start|>user
/// Hello<|im_end|>
/// <|im_start|>assistant
/// ```
fn format_prompt(messages: &[ChatMessage], tools: Option<&[Tool]>) -> String {
    let mut prompt = String::new();

    // If tools are provided, inject them into the system message
    if let Some(tools) = tools {
        if !tools.is_empty() {
            prompt.push_str("<|im_start|>system\n");
            // Find existing system message content
            let system_content: String = messages
                .iter()
                .filter(|m| m.role == Role::System)
                .filter_map(|m| m.text_content())
                .collect::<Vec<_>>()
                .join("\n");

            if !system_content.is_empty() {
                prompt.push_str(&system_content);
                prompt.push_str("\n\n");
            }

            prompt.push_str("You have access to the following tools:\n\n");
            for tool in tools {
                let tool_json = serde_json::json!({
                    "type": "function",
                    "function": {
                        "name": tool.function.name,
                        "description": tool.function.description,
                        "parameters": tool.function.parameters,
                    }
                });
                prompt.push_str(&serde_json::to_string_pretty(&tool_json).unwrap_or_default());
                prompt.push('\n');
            }
            prompt.push_str("\nTo call a tool, respond with a JSON object in this exact format:\n");
            prompt.push_str(r#"{"tool_calls": [{"id": "call_<unique_id>", "type": "function", "function": {"name": "<tool_name>", "arguments": "<json_arguments>"}}]}"#);
            prompt.push_str("\n\nIf you don't need to call a tool, respond normally with text.\n");
            prompt.push_str("<|im_end|>\n");

            // Now add non-system messages
            for msg in messages {
                if msg.role == Role::System {
                    continue; // Already handled above
                }
                let role = match msg.role {
                    Role::User => "user",
                    Role::Assistant => "assistant",
                    Role::Tool => "tool",
                    Role::System => unreachable!(),
                };
                let content = msg.text_content().unwrap_or("");
                prompt.push_str(&format!(
                    "<|im_start|>{}\n{}<|im_end|>\n",
                    role, content
                ));
            }
        } else {
            // No tools, format normally
            format_messages_chatml(&mut prompt, messages);
        }
    } else {
        format_messages_chatml(&mut prompt, messages);
    }

    // Open the assistant turn for generation
    prompt.push_str("<|im_start|>assistant\n");
    prompt
}

/// Format messages in standard ChatML without tool definitions.
fn format_messages_chatml(prompt: &mut String, messages: &[ChatMessage]) {
    for msg in messages {
        let role = match msg.role {
            Role::System => "system",
            Role::User => "user",
            Role::Assistant => "assistant",
            Role::Tool => "tool",
        };
        let content = msg.text_content().unwrap_or("");
        prompt.push_str(&format!(
            "<|im_start|>{}\n{}<|im_end|>\n",
            role, content
        ));
    }
}

