/// Attempt to parse tool calls from generated text.
///
/// Supports two formats:
/// 1. `{"tool_calls": [{"id": "...", "type": "function", "function": {...}}]}`
/// 2. `{"name": "...", "arguments": {...}}` (simpler format some models use)
fn parse_tool_calls(text: &str) -> Option<Vec<ToolCall>> {
    let trimmed = text.trim();

    // Try to find JSON in the text (model might wrap it in markdown or other text)
    let json_str = extract_json_block(trimmed)?;

    // Attempt format 1: full tool_calls array
    if let Ok(parsed) = serde_json::from_str::<serde_json::Value>(json_str) {
        if let Some(tool_calls) = parsed.get("tool_calls").and_then(|v| v.as_array()) {
            let calls: Vec<ToolCall> = tool_calls
                .iter()
                .filter_map(|tc| {
                    let id = tc
                        .get("id")
                        .and_then(|v| v.as_str())
                        .unwrap_or("call_0")
                        .to_string();
                    let func = tc.get("function")?;
                    let name = func.get("name")?.as_str()?.to_string();
                    let arguments = func.get("arguments").map(|a| {
                        if a.is_string() {
                            a.as_str().unwrap_or("{}").to_string()
                        } else {
                            a.to_string()
                        }
                    }).unwrap_or_else(|| "{}".to_string());

                    Some(ToolCall {
                        id,
                        call_type: "function".to_string(),
                        function: FunctionCall { name, arguments },
                    })
                })
                .collect();

            if !calls.is_empty() {
                return Some(calls);
            }
        }

        // Attempt format 2: single function call
        if let (Some(name), Some(arguments)) = (
            parsed.get("name").and_then(|v| v.as_str()),
            parsed.get("arguments"),
        ) {
            let arguments_str = if arguments.is_string() {
                arguments.as_str().unwrap_or("{}").to_string()
            } else {
                arguments.to_string()
            };

            return Some(vec![ToolCall {
                id: format!("call_{}", uuid::Uuid::now_v7()),
                call_type: "function".to_string(),
                function: FunctionCall {
                    name: name.to_string(),
                    arguments: arguments_str,
                },
            }]);
        }
    }

    None
}

/// Extract a JSON block from text that may contain surrounding prose.
fn extract_json_block(text: &str) -> Option<&str> {
    // Look for ```json ... ``` code block first
    if let Some(start) = text.find("```json") {
        let json_start = start + 7;
        if let Some(end) = text[json_start..].find("```") {
            return Some(text[json_start..json_start + end].trim());
        }
    }

    // Look for ``` ... ``` code block
    if let Some(start) = text.find("```") {
        let code_start = start + 3;
        // Skip optional language identifier on the same line
        let content_start = text[code_start..]
            .find('\n')
            .map(|i| code_start + i + 1)
            .unwrap_or(code_start);
        if let Some(end) = text[content_start..].find("```") {
            return Some(text[content_start..content_start + end].trim());
        }
    }

    // Try to find a top-level JSON object in the text
    let first_brace = text.find('{')?;
    let mut depth = 0i32;
    let mut last_brace = first_brace;

    for (i, ch) in text[first_brace..].char_indices() {
        match ch {
            '{' => depth += 1,
            '}' => {
                depth -= 1;
                if depth == 0 {
                    last_brace = first_brace + i;
                    break;
                }
            }
            _ => {}
        }
    }

    if depth == 0 && last_brace > first_brace {
        Some(&text[first_brace..=last_brace])
    } else {
        None
    }
}

