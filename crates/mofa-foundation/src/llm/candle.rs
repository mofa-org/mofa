/// Extract embeddings by running a forward pass and pooling hidden states.
fn extract_embeddings_sync(
    engine: &mut InferenceEngine,
    texts: &[String],
    pooling: &EmbeddingPooling,
) -> Result<Vec<Vec<f32>>, LLMError> {
    let map_err = |e: candle_core::Error| LLMError::Other(format!("Candle error: {}", e));

    let mut embeddings = Vec::with_capacity(texts.len());

    for text in texts {
        engine.model.reset();

        let tokens = engine.tokenizer.encode(text)?;
        if tokens.is_empty() {
            // Return zero vector for empty input
            embeddings.push(vec![0.0f32; engine.hidden_size]);
            continue;
        }

        let input = Tensor::new(&tokens[..], &engine.device).map_err(map_err)?;
        let input = input.unsqueeze(0).map_err(map_err)?;

        // Try to get hidden states; fall back to logits-based approximation
        let hidden = match engine.model.forward_hidden(&input, 0) {
            Ok(h) => h,
            Err(_) => {
                // Use the logits output as a proxy — this is a rough approximation
                // but works for basic similarity tasks
                let logits = engine.model.forward(&input, 0).map_err(map_err)?;
                logits
            }
        };

        // hidden shape: [1, seq_len, hidden_size] or [1, seq_len, vocab_size]
        let hidden = hidden.squeeze(0).map_err(map_err)?; // [seq_len, dim]
        let seq_len = hidden.dim(0).map_err(map_err)?;

        let pooled = match pooling {
            EmbeddingPooling::Mean => {
                hidden
                    .sum(0)
                    .map_err(map_err)?
                    .to_dtype(DType::F32)
                    .map_err(map_err)?
                    .affine(1.0 / seq_len as f64, 0.0)
                    .map_err(map_err)?
            }
            EmbeddingPooling::FirstToken => {
                hidden
                    .get(0)
                    .map_err(map_err)?
                    .to_dtype(DType::F32)
                    .map_err(map_err)?
            }
            EmbeddingPooling::LastToken => {
                hidden
                    .get(seq_len - 1)
                    .map_err(map_err)?
                    .to_dtype(DType::F32)
                    .map_err(map_err)?
            }
        };

        // L2 normalize
        let norm = pooled
            .sqr()
            .map_err(map_err)?
            .sum_all()
            .map_err(map_err)?
            .sqrt()
            .map_err(map_err)?
            .to_scalar::<f32>()
            .map_err(map_err)?;

        let normalized = if norm > 0.0 {
            pooled.affine(1.0 / norm as f64, 0.0).map_err(map_err)?
        } else {
            pooled
        };

        let embedding: Vec<f32> = normalized.to_vec1().map_err(map_err)?;
        embeddings.push(embedding);
    }

    Ok(embeddings)
}

