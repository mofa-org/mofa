/// Supported model architectures for auto-detection.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
enum ModelArch {
    Llama,
    Mistral,
    Phi,
    Qwen2,
}

/// A simple JSON config.json reader that detects the architecture.
fn detect_architecture(model_path: &Path) -> Result<ModelArch, LLMError> {
    let config_path = model_path.join("config.json");
    let config_text = std::fs::read_to_string(&config_path).map_err(|e| {
        LLMError::ConfigError(format!("Cannot read {:?}: {}", config_path, e))
    })?;
    let config: serde_json::Value = serde_json::from_str(&config_text)
        .map_err(|e| LLMError::ConfigError(format!("Invalid config.json: {}", e)))?;

    let arch_str = config
        .get("architectures")
        .and_then(|a| a.as_array())
        .and_then(|a| a.first())
        .and_then(|v| v.as_str())
        .or_else(|| config.get("model_type").and_then(|v| v.as_str()))
        .unwrap_or("");

    let arch_lower = arch_str.to_lowercase();

    if arch_lower.contains("llama") {
        Ok(ModelArch::Llama)
    } else if arch_lower.contains("mistral") {
        Ok(ModelArch::Mistral)
    } else if arch_lower.contains("phi") {
        Ok(ModelArch::Phi)
    } else if arch_lower.contains("qwen2") {
        Ok(ModelArch::Qwen2)
    } else {
        tracing::warn!(
            "Unknown model architecture '{}', falling back to Llama",
            arch_str
        );
        Ok(ModelArch::Llama)
    }
}

/// Read the hidden size from config.json for embedding dimension.
fn read_hidden_size(model_path: &Path) -> Result<usize, LLMError> {
    let config_path = model_path.join("config.json");
    let config_text = std::fs::read_to_string(&config_path).map_err(|e| {
        LLMError::ConfigError(format!("Cannot read {:?}: {}", config_path, e))
    })?;
    let config: serde_json::Value = serde_json::from_str(&config_text)
        .map_err(|e| LLMError::ConfigError(format!("Invalid config.json: {}", e)))?;

    config
        .get("hidden_size")
        .and_then(|v| v.as_u64())
        .map(|v| v as usize)
        .ok_or_else(|| LLMError::ConfigError("hidden_size not found in config.json".to_string()))
}

