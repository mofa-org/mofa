/// Find all SafeTensors weight files in a directory.
fn find_safetensor_files(dir: &Path) -> Vec<PathBuf> {
    let mut files: Vec<PathBuf> = std::fs::read_dir(dir)
        .into_iter()
        .flatten()
        .filter_map(|e| e.ok())
        .map(|e| e.path())
        .filter(|p| {
            p.extension()
                .map(|ext| ext == "safetensors")
                .unwrap_or(false)
        })
        .collect();
    files.sort();
    files
}

fn load_model(
    arch: ModelArch,
    model_path: &Path,
    device: &Device,
    dtype: DType,
) -> Result<Box<dyn CandleModel>, LLMError> {
    let config_path = model_path.join("config.json");
    let config_text = std::fs::read_to_string(&config_path)
        .map_err(|e| LLMError::ConfigError(format!("Cannot read config.json: {}", e)))?;

    let weight_files = find_safetensor_files(model_path);
    if weight_files.is_empty() {
        return Err(LLMError::ConfigError(format!(
            "No .safetensors files found in {:?}",
            model_path
        )));
    }

    let vb = unsafe {
        candle_core::safetensors::MmapedSafetensors::multi(&weight_files)
            .map_err(|e| LLMError::ConfigError(format!("Failed to mmap weights: {}", e)))?
    };
    let vb =
        candle_core::nn::VarBuilder::from_mmaped_safetensors(vec![vb], dtype, device)
            .map_err(|e| LLMError::ConfigError(format!("VarBuilder error: {}", e)))?;

    match arch {
        ModelArch::Llama | ModelArch::Mistral | ModelArch::Phi | ModelArch::Qwen2 => {
            if !matches!(arch, ModelArch::Llama) {
                tracing::info!(
                    "Architecture {:?} using Llama-compatible loader",
                    arch
                );
            }

            let llama_config: candle_transformers::models::llama::LlamaConfig =
                serde_json::from_str(&config_text).map_err(|e| {
                    LLMError::ConfigError(format!("Invalid Llama config: {}", e))
                })?;
            // use_flash_attn = false for maximum compatibility
            let config = llama_config.into_config(false);
            let cache = candle_transformers::models::llama::Cache::new(
                true, dtype, &config, device,
            )
            .map_err(|e| LLMError::ConfigError(format!("Cache init failed: {}", e)))?;
            let model = candle_transformers::models::llama::Llama::load(vb, &config)
                .map_err(|e| LLMError::ConfigError(format!("Model load failed: {}", e)))?;
            Ok(Box::new(LlamaModel { model, cache }))
        }
    }
}

